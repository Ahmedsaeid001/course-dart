void main() {
  //* قدر يحدد ان القيمة المدخله دي رقم صحيح int / num
  //* compiler شافها كدا وخنها في الذاكره int
  num num1 = 60;
  double num2 = 50.5;
  print('num1 = $num1,num2 = $num2');

  num result = num1 + num2;

  //* num قمنا بإستخدمها حددت الإجابة لوحدها ان النتيجة دي برقم عشري يعني double
  print('result = $result');
}
