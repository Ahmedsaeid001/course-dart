void main() {
  //* and &&
  print('--------logical And--------');
  print('true && false = ${true && false}'); //false
  print('true && true=${true && true}'); // true

  //* or ||
  print('--------logical OR--------');
  print('false || true = ${false || true}'); //false
  print('false || false=${false || false}'); // true

//* not !
  print('--------logical Not--------');
  print('!true=> ${!true}'); // false
  print('!false=> ${!false}'); // true
}
